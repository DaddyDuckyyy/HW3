public class Variables {
    public static void main(String[] args) {
        double v1 = 27.12;
        long v2 = 987678965549L;
        float v3 = 2.786f;
        short v4 = 569;
        short v5 = -159;
        int v6 = 27897;
        byte v7 = 67;

        System.out.println("Значение переменной v1 с типом double равно " + v1);
        System.out.println("Значение переменной v2 с типом long равно " + v2);
        System.out.println("Значение переменной v3 с типом float равно " + v3);
        System.out.println("Значение переменной v4 с типом short равно " + v4);
        System.out.println("Значение переменной v5 с типом short равно " + v5);
        System.out.println("Значение переменной v6 с типом int равно " + v6);
        System.out.println("Значение переменной v7 с типом byte равно " + v7);
    }
}
